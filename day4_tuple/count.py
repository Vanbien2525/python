colors = ("red", "blue", "green", "red", "red", "blue")

# a. Đếm số lần xuất hiện của "red"
count_red = colors.count("red")
print("So lan xuat hien cua red la: ", count_red)

# b. Tìm vị trí đầu tiên của "blue"
index_blue = colors.index("blue")
print("Vi tri dau tien blue xuat hien la: ", index_blue)